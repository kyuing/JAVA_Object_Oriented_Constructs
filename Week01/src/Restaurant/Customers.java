package Restaurant;

public class Customers {
	
	private String fname;
	private String contact;
	
	public Customers(String fname, String contact) {
		
		this.fname = fname;
		this.contact = contact;
	}
	
	public String getFname() {
		
		return this.fname;
	}
	
	public String getContact() {
		
		return this.contact;
	}
	

}
